#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int res;

	res = 0;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n = -n;
		write(fd, "-", 1);
	}
	if (n <= 9)
	{
		res = n + '0';
		write(fd, &res, 1);
	}
	else
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
