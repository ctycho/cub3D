#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*c;

	i = 0;
	while (s1[i])
		i++;
	c = (char *)malloc(i + 1);
	if (c == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		c[i] = s1[i];
		i++;
	}
	c[i] = '\0';
	return (c);
}
