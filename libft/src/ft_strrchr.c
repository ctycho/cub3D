#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *find;

	find = 0;
	while (*s)
	{
		if (*s == c)
			find = ((char *)s);
		s++;
	}
	if (*s == 0 && c == 0)
		return ((char *)s);
	return (find);
}
